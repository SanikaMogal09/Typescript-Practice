// Literal types allows you to specify a value that can only be of one specific literal value
// This means that a variable with a literal type can have only one specific value and no other.

// string literals
let colors : "red"|"blue"|"green";
colors = "red";
// colors="yellow"; invalid


// Number literals
let numss :1 |4 |5;
numss = 4;
// numss = 6; invalid

// Boolean literals
let IsTrue : true;
// IsTrue=false; invalid


