// Multi dimensional Array 

// A multi dimensional array is a array that contains other arrays as its elements.

// Multi dimensional arrays can be defined using the same notation as one-dimensional arrays, but with nested square brackets.

const singledi :number[] =[1,2,3,4];
const doubledi : number[][] = [[1,2,3,4,5]];
const tripledi : number[][][] = [[[1,2,3,4,5]]];
console.log(singledi);
console.log(doubledi);
console.log(tripledi);
